package Herhalingen_1_10;

public class H04_DataCasting {
    public static void main(String[] args) {
        int sayi1=(int)7.3;
        System.out.println("sayi1 : " + sayi1); // 7
        double sayi2=10;
        System.out.println("sayi2 :" + sayi2); // 10.0
        int sayi3='c';
        System.out.println("sayi3 : " + sayi3); // 99
        char harf2= 98;
        System.out.println("harf2 : "+ harf2); // b
        char harf3= 100;
        System.out.println("harf3 = " + harf3);
    }
}
